        <p>
            As hockey entered the 1980s, players started to shoot higher and faster shots on goaltenders. For decades,
            goaltenders had utilized a style of play called "Stand-up" where they would remain on their feet to make a
            majority of their saves, usually with their glove hand or by kicking out their legs. This was because most
            shots were low and fast. Hall of Fame players such as Jacque Plante were masters of this style of play during
            the time.
        </p>
