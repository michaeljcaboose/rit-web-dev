            Today, with the speed of the game and the talent and abilities of the skaters shooting on them, goaltenders
            mix and match techniques while being more mobile and athletic than their predecessors. Most goaltenders are
            considered to be hybrid, stopping the pucks any way they can rather than sticking to one concrete style,
            though some goaltenders are notably "butterfly"-only goalies.
