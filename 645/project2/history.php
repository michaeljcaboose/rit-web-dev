        <p>
            As the game of hockey changed for skaters, goaltenders needed to adjust to these new shot patterns. The 80s
            and 90s saw goalies transition from "Stand-up" to a style called "Butterfly" where goalies would drop to their
            knees and flair their legs out to cover the bottom of the net. The pioneer of this style in the late 80s and 90s
            was Patrick Roy, who is considered one of the greatest goaltenders of all time.
        </p>
        <figure>
            <img src="media/roy.jpg">
