        <p>
            As this style became more popular, the gear started changing to fit the style, and eventually players where
            put into two groups: "Butterfly" and "Hybrid", with hybrid being a mixture of the "Stand-up" and "Butterfly"
            styles. All-time great goaltenders such as Dominique Hasek and Martin Brodeur were known to play more of a hybrid
            style, utilizing butterfly techniques while still being flexible in their save selection styles.
        </p>
