        <div class="div_top-space" id="legpad">
	        <h2>Leg Pads</h2>
	        <p>The modern leg pad is usually the most expensive piece of equipment a goaltender will buy in their kit. They are what goaltenders where to protect their legs from anything coming their way on the ice, from pucks to sticks and skates. Goaltender preference and style of play are a huge factor when it comes to leg pads, from the number of rolls and breakpoints where a pad will bend, down to the number of straps and positioning of the internal foam pieces for leg protection.
		        
	        </p>
	        <p>
		        Let’s start with the type of pad a goaltender will wear. There are two types of pads, butterfly and hybrid; both come with their own advantages that tailor to the wearers playing style. The first question to ask is what type of player you are: are you truly a butterfly goaltender, focusing on technique and positioning, or do you use more athleticism and reaction when you make saves?
	        </p>
	        <p>
		        Butterfly style pads have flat surfaces, are stiffer, have increased knee cushioning, and are designed to maintain more pad contact with the ice. Hybrid pads have a much more flexible service and are angled towards the top of the pad to allow for more agile movement. This isn't to say that hybrid goaltenders shouldn't wear butterfly style pads or vice-versa, but to point out that they are designed to maximize certain aspects of your play style.
	        </p>
            <figure>
                <img src="media/butterfly-hybrid-goalie-leg-pads.jpg">
                <figcaption>Butterfly pads on the left, Hybrid pads on the right.</figcaption>
            </figure>
	        <p>Construction materials are where you see your monetary differences between different sets of pads. More expensive pads will often contain proprietary foam and insulation materials and have multiple layers of each, where cheaper entry level and competitive level pads will either have natural materials or only one layer of material.</p>
	        <p>
		        Strapping systems are where a lot of customization happens for pro and competitive level gear. Most high quality gear uses leather straps and metal buckles, with goaltenders sighting durability, fit, and comfort for their choice over using Nylon straps and "Alligator" clips, though some goaltenders do prefer the convenience of them.
	        </p>
	        <p>
		        The leather used on the outer shell of padding is synthetic, which is lighter and more cost-effective than genuine leather and is also more durable and less likely to maintain moisture that could wear down the outer shell over time. Look for this material over others such as heavy gauge nylon or Cordura, since those materials are used for street and ball hockey and will not stand up well to the rigors of ice hockey.
	        </p>
	        <p>
		        Sizing of leg pads is a preference for most goaltenders, but general rule for sizing is (skate-size + ankle-to-knee + knee to wherever you want your pads to stop). All manufacturers have different sizing systems and some goalies like pads that may be "smaller" or "larger" than their calculated fit. 
	        </p>
