	    </div>
        <div class="div_top-space" id="pants">
	        <h2>Pants</h2>
	        <p>Sometimes the puck will make it past your leg pads, catcher, and blocker. Goaltenders never intend to stop the puck with their thighs and hips, but sometimes shots find their way through everything else to make it there. In that scenario, the goaltender needs to have a flexible yet protective piece of equipment protect their knees, thighs, and hips.
		        
	        </p>
            <figure>
                <img src="media/goalie-pants.jpg">
                <figcaption>Goaltender pants</figcaption>
            </figure>
	        <p>
		        Goaltender pants are of course filled with dense foam padding for a comfortable fit, as well as hard plastic inserts that provide protection to the middle area of the body. The level of protection and fit of course depends on the quality of the materials used. Elite goaltenders need more protection, while recreational and semi competitive players may not need the same level of protection as the higher quality gear provides. 
	        </p>
	        <p>
		        Some pants come may come with built in thigh and knee protection, but in most cases those items may have to be purchased separately. If your pants do not come with knee protection, my advise would be to buy a pair of knee and thigh guards. Taking even a 30 to 40 mph shot directly to the knee is not a fun experience and could cause serious harm.
	        </p>
	        <p>
		        The best way to try on goalie pants is to wear them with your leg pads. This way you can check to make sure you aren't exposing areas of your body to shots you're trying to save, as well as general feel and comfort. 
	        </p>
