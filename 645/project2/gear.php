        <div class="div_top-space" id="blocker">
	        <h2>Blocker</h2>
            <figure>
                <img src="media/blocker.jpg">
                <figcaption>The Blocker</figcaption>
            </figure>
	        <p>Blockers are worn by goaltenders to protect the stick-wielding hand. They are simple in design as their sole purpose is to protect the stick hand. They are angled forward at the top of the blocker to prevent the puck from deflecting up and over the goaltender after the puck makes contact with the blocker. The angling also helps push the puck low and away from the net. The thickness of the board may vary from level to level, though the preference now is to have heavier boards to soak up more force from incoming pucks, as well as provide the goaltender with better control when attempting to redirect the puck. As with all pieces of equipment, higher end models are crafted with higher quality materials.</p>
