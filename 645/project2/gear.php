        <div class="div_top-space" id="catcher">
	        <h2>Catcher</h2>
	        <p>
		        Catchers, otherwise called trappers or simply, gloves, are worn on the "catching" hand of a goaltender to protect the hand as well as cover and catch pucks to defend the net. There are two key features of a catcher: the binding and the web. 
	        </p>
	        <p>
		        Binding is the solid piece of foam that protects the wrist, arm, and hand from the puck. The foam is either bound to the glove as its own  piece, or "binding-less" where it is seamlessly integrated into the glove. Binding helps guide the puck into the webbing while "binding-less" gloves have a larger overall blocking surface.
	        </p>
            <figure>
                <img src="media/catcher-break.jpg">
                <figcaption>Catcher with Binding</figcaption>
            </figure>
            <figure>
                <img src="media/catcher-bindless.jpg">
                <figcaption>Bindless Catcher</figcaption>
            </figure>
	        <p>
		        Webbing can be either "Single-T" or "Double-T". The T refers to the piece of material that supports the webbing of the glove. "Single-T" gloves have less webbing, making it easier for goaltenders to drop the puck from their glove, while "Double-T"  gloves have more webbing and trap the puck more effectively. 
	        </p>
