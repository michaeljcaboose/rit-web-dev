	        <h2>Chest</h2>
            <figure>
                <img src="media/chest-protector.jpg">
                <figcaption>The chest protector</figcaption>
            </figure>
	        <p>
	        	Chest protectors are pretty self explanatory. They cover the entirety of the goaltender's torso, as well as their arms. They are built using various types of foam in numerous segments depending on the upper body area. </p><p>Higher quality protectors will of course have more strategic layers and segmentation of padding than lower quality protectors. Higher quality protectors will also, of course, have more options for making adjustments and provide better body coverage overall, but even low level protectors should cover a minimum area of your full arm from shoulder to wrist; neck to hips, overlapping with your pants, and all the way around both sides of your rib cage.
        	</p>
